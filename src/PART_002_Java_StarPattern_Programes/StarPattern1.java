package PART_002_Java_StarPattern_Programes;

public class StarPattern1 {

	public static void main(String[] args) {
	
           for(int i=0;i<6;i++) 
           {
        	   //System.out.println("*");
        	   for(int j=0;j<=i;j++) 
        	   {
        		  System.out.print("*"); 
        	   }
        	   System.out.println();
           }
		
	}

}
                 
