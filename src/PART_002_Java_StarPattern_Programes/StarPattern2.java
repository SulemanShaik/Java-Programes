package PART_002_Java_StarPattern_Programes;


public class StarPattern2 {

	public static void main(String[] args) {
        
		for (int i=6;i>=1;i--) 
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print("*");
			}
			 System.out.println();
		}
	}

}







